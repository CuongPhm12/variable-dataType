public class Main {
    public static void main(String[] args) {
        int i = 10;//khai báo biến i có kiểu giá trị là 10
        float f = 20.5f; //khai báo biến f có kiểu float giá trị là 20.5
        double d =20.5;//khai báo biến d có kiểu double giá trị là 20.5
        boolean b =true;//khai báo biến b có kiểu boolean có giá trị là true
        char c = 'a';//khai báo biến c có kiểu chả có giá trị là ký tự a
        String s = "Hà Nội";//khai báo biến s có kiểu string có giá trị là chuỗi Hà Nội
        System.out.println("i = "+i);
        System.out.println("f = "+f);
        System.out.println("d = "+d);
        System.out.println("b = " +b);
        System.out.println("c = "+c);
        System.out.println("s = "+ s);


    }
}
